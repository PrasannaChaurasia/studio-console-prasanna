export function featuredProjects(projects = fallbackProjects) {
  return projects.filter((project) => project.published).sort((a, b) => a.sort_order - b.sort_order);
}

export function projectBySlug(slug, projects = fallbackProjects) {
  return projects.find((project) => project.slug === slug && project.published);
}
